function multiplicaMatriz(matriz){
    let linhas = matriz.length-1
    let colunas = matriz[0].length-1

    let matrizMultiplicada = Array.from({length: linhas}, () => Array(colunas).fill(0))

    for(let i=0;i<linhas;i++){
        let maiorValorLinha = matriz[i][i]
        for(let j=0;j<colunas;j++){
            matrizMultiplicada[i][j] = (matriz[i][j] * maiorValorLinha)
        }
    }

    return matrizMultiplicada
}

